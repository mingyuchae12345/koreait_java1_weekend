package com.kita.first.level1;

public class f2 {
	public static void main(String[] args) {
		int n1 = 2;
		int n2 = 3;
		
		// if문 사용해서 n1과 n2 비교
		// n1이 n2 보다 크다.
		// n1이 n2보다 작다.
		// n1과 n2의 값이 같다.
		
		if(n1 >= n2);
		System.out.println("n1 이 더 큽니다");
		if(n1 <= n2);
		System.out.println("n1 이 더 작습니다");
		if(n1 == n2);
		System.out.println("값이 같습니다");
		
	}
}
